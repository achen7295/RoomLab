package People;

public class Destruction {
    int xdLoc, ydLoc;

    public int getxdLoc(){
        return xdLoc;
    }

    public int getydLoc(){
        return ydLoc;
    }

    public void setxdLoc(int xdLoc){
        this.xdLoc = xdLoc;
    }

    public void setydLoc(int ydLoc){
        this.ydLoc = ydLoc;
    }

    public Destruction(int xdLoc, int ydLoc){
        this.xdLoc = xdLoc;
        this.ydLoc = ydLoc;
    }

}
